public class Horse {
    private String name;
    private String colour;
    private int birthYear;

    public String getName() {
        return name;
    }
    public String getColour() {
        return colour;
    }
    public int getBirthYear() {
        return birthYear;
    }
    public void setName(String n){
        name = n;
    }
    public void setColour(String c){
        colour = c;
    }
    public void setBirthYear(int x){
        birthYear = x;
    }
}

